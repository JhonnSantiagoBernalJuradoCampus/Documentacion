//Operadores Aritmeticos (libreria matematica)
//Metodo Math.sqrt(), La función Math.sqrt() retorna la raíz cuadrada de un número, Sintaxis Math.sqrt(x), Valor de Retorno La raíz cuadrada del número proporcionado. Si los números son negativos, NaN es devuelto.
alert(Math.sqrt(9))
//El resultado es 3
alert(Math.sqrt(-9))
//El resultado es NaN
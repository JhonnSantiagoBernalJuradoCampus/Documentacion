//Metodo indexOf
//El método lastIndexOf() devuelve la posicion (indice) en la que se encuentra el valorBusqueda, dentro del objeto String que realiza la llamada, de la última ocurrencia del valor especificado; o -1 si no se halla. La búsqueda se realiza empezando por el final de la cadena que realiza la llamada.
let cadena = "Santiago Bernal";
let indice = cadena.lastIndexOf("a");
alert(indice)
//El resultado es 13 ya que toma la primera aparicion del valor a empezando desde el final de la cadena